#!/usr/bin/env node
import { execFileSync } from "node:child_process";
import { mkdirSync, writeFileSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const DEFAULT_TERRAFORM_DIR = "../StockBrief-be/infra/terraform";
const DEFAULT_OUTPUT_PATH = ".env.local";
const DEFAULT_REDIRECT_URI = "http://localhost:3001/auth/callback";
const DEFAULT_REGION = "ap-northeast-2";

const REQUIRED_OUTPUTS = [
  "api_base_url",
  "cognito_app_client_id",
  "cognito_hosted_ui_domain",
  "cognito_user_pool_id",
];

export function parseArgs(argv) {
  const options = {
    terraformDir: DEFAULT_TERRAFORM_DIR,
    outputPath: DEFAULT_OUTPUT_PATH,
    redirectUri: DEFAULT_REDIRECT_URI,
    region: DEFAULT_REGION,
    dryRun: false,
  };

  for (let index = 0; index < argv.length; index += 1) {
    const arg = argv[index];
    if (arg === "--terraform-dir") {
      options.terraformDir = readValue(argv, (index += 1), arg);
    } else if (arg === "--output") {
      options.outputPath = readValue(argv, (index += 1), arg);
    } else if (arg === "--redirect-uri") {
      options.redirectUri = readValue(argv, (index += 1), arg);
    } else if (arg === "--region") {
      options.region = readValue(argv, (index += 1), arg);
    } else if (arg === "--dry-run") {
      options.dryRun = true;
    } else if (arg === "--help" || arg === "-h") {
      options.help = true;
    } else {
      throw new Error(`Unknown argument: ${arg}`);
    }
  }

  return options;
}

export function readTerraformOutputs(terraformDir) {
  const raw = execFileSync("terraform", ["output", "-json"], {
    cwd: terraformDir,
    encoding: "utf8",
    stdio: ["ignore", "pipe", "pipe"],
  });
  return JSON.parse(raw);
}

export function renderFrontendEnv(terraformOutputs, options = {}) {
  const outputValues = unwrapTerraformOutputs(terraformOutputs);
  const missing = REQUIRED_OUTPUTS.filter((key) => !outputValues[key]);
  if (missing.length > 0) {
    throw new Error(`Missing required Terraform output(s): ${missing.join(", ")}`);
  }

  const issuerRegion = regionFromIssuer(outputValues.cognito_issuer);
  const region = options.region ?? issuerRegion ?? DEFAULT_REGION;
  const redirectUri = options.redirectUri
    ?? (outputValues.frontend_hosted_url
      ? `${String(outputValues.frontend_hosted_url).replace(/\/+$/, "")}/auth/callback`
      : DEFAULT_REDIRECT_URI);

  return [
    "# Generated by pnpm run sync:dev-env. Do not commit this file.",
    `NEXT_PUBLIC_API_BASE_URL=${ensureV1ApiBase(outputValues.api_base_url)}`,
    `NEXT_PUBLIC_COGNITO_REGION=${region}`,
    `NEXT_PUBLIC_COGNITO_USER_POOL_ID=${outputValues.cognito_user_pool_id}`,
    `NEXT_PUBLIC_COGNITO_APP_CLIENT_ID=${outputValues.cognito_app_client_id}`,
    `NEXT_PUBLIC_COGNITO_HOSTED_UI_DOMAIN=${outputValues.cognito_hosted_ui_domain}`,
    `NEXT_PUBLIC_COGNITO_REDIRECT_URI=${redirectUri}`,
    "",
  ].join("\n");
}

export function unwrapTerraformOutputs(terraformOutputs) {
  return Object.fromEntries(
    Object.entries(terraformOutputs).map(([key, output]) => [
      key,
      output && typeof output === "object" && "value" in output ? output.value : output,
    ]),
  );
}

export function ensureV1ApiBase(apiBaseUrl) {
  const normalized = String(apiBaseUrl).replace(/\/+$/, "");
  return normalized.endsWith("/v1") ? normalized : `${normalized}/v1`;
}

export function regionFromIssuer(issuer) {
  if (typeof issuer !== "string") return null;
  const match = issuer.match(/^https:\/\/cognito-idp\.([a-z0-9-]+)\.amazonaws\.com\//);
  return match?.[1] ?? null;
}

export function writeEnvFile(outputPath, content) {
  mkdirSync(path.dirname(outputPath), { recursive: true });
  writeFileSync(outputPath, content, { encoding: "utf8", mode: 0o600 });
}

function readValue(argv, index, flag) {
  const value = argv[index];
  if (!value || value.startsWith("--")) {
    throw new Error(`${flag} requires a value.`);
  }
  return value;
}

function printHelp() {
  console.log(`Usage: pnpm run sync:dev-env -- [options]

Options:
  --terraform-dir <path>  BE Terraform directory. Default: ${DEFAULT_TERRAFORM_DIR}
  --output <path>         Env file to write. Default: ${DEFAULT_OUTPUT_PATH}
  --redirect-uri <uri>    Cognito callback URI. Default: ${DEFAULT_REDIRECT_URI}
  --region <region>       Cognito region fallback. Default: ${DEFAULT_REGION}
  --dry-run               Print the generated file instead of writing it.
`);
}

function main() {
  const options = parseArgs(process.argv.slice(2));
  if (options.help) {
    printHelp();
    return;
  }

  const terraformDir = path.resolve(process.cwd(), options.terraformDir);
  const outputPath = path.resolve(process.cwd(), options.outputPath);
  const content = renderFrontendEnv(readTerraformOutputs(terraformDir), options);

  if (options.dryRun) {
    process.stdout.write(content);
    return;
  }

  writeEnvFile(outputPath, content);
  console.log(`Wrote ${path.relative(process.cwd(), outputPath)} from ${terraformDir}`);
}

const currentFilePath = fileURLToPath(import.meta.url);
if (process.argv[1] && path.resolve(process.argv[1]) === currentFilePath) {
  try {
    main();
  } catch (error) {
    console.error(error instanceof Error ? error.message : String(error));
    process.exitCode = 1;
  }
}
